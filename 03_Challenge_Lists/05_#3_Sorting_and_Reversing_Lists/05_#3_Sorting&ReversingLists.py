fruits = ['banana', 'apple', 'grape', 'kiwi', 'orange']
fruits.sort() #sort fruits in alphabetical order
print(fruits) #print in alphabetical order
fruits.sort(reverse=True) #sort fruits in reverse alphabetical order
print(fruits) #print in reverse alphabetical order