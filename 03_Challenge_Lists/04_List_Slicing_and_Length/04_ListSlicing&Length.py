colors = ['red', 'blue', 'green', 'yellow', 'purple'] #list of colors
print(colors)
sliced_colors = colors[1:3] #slice color 'blue' and 'green' from the list
print(sliced_colors) #print the modified list
print("length:",len(colors)) #print the length of the list of colors